using System;
{
    class Program
    {
        static int[] СоздатьМассив(int n)
        {
            Random rnd = new Random();
            int[] массив = new int[n];
            for (int i = 0; i < n; i++)
            {
                массив[i] = rnd.Next(-100, 101);
            }
            return массив;
        }       
        static int СуммаЧетныхИндексов(int[] массив)
        {
            int сумма = 0;
            for (int i = 0; i < массив.Length; i += 2)
            {
                сумма += массив[i];
            }
            return сумма;
        }    
       static int МинимальныйПоложительный(int[] массив)
        {
            int? минимум = null;
            foreach (int элемент in массив)
            {
                if (элемент > 0)
                {
                    if (минимум == null || элемент < минимум)
                        минимум = элемент;
                }
            }
            return минимум ?? 0;
        }       
        static int СуммаМаксМин(int[] массив)
        {
            int макс = массив[0];
            int мин = массив[0];
            foreach (int элемент in массив)
            {
                if (элемент > макс) макс = элемент;
                if (элемент < мин) мин = элемент;
            }
            return макс + мин;
        }        
        static int КоличествоПоложительных(int[] массив)
        {
            int количество = 0;
            foreach (int элемент in массив)
            {
                if (элемент > 0) количество++;
            }
            return количество;
        }
        
        static int СуммаЧетныхПоложительныхВДиапазоне(int[] массив, int a, int b)
        {
            int сумма = 0;
            for (int i = a; i <= b && i < массив.Length; i++)
            {
                if (массив[i] > 0 && массив[i] % 2 == 0)
                {
                    сумма += массив[i];
                }
            }
            return сумма;
        }       
        static double СреднееЧетных(int[] массив)
        {
            int сумма = 0;
            int количество = 0;
            foreach (int элемент in массив)
            {
                if (элемент % 2 == 0)
                {
                    сумма += элемент;
                    количество++;
                }
            }
            return (double)сумма / количество;
        }     
        static int СуммаЧетныхПоложительных(int[] массив)
        {
            int сумма = 0;
            foreach (int элемент in массив)
            {
                if (элемент > 0 && элемент % 2 == 0)
                {
                    сумма += элемент;
                }
            }
            return сумма;
        }    
        static int СуммаНеотрицательныхНаЧетныхМестах(int[] массив)
        {
            int сумма = 0;
            for (int i = 0; i < массив.Length; i += 2)
            {
                if (массив[i] >= 0)
                {
                    сумма += массив[i];
                }
            }
            return сумма;
        }      
        static (int максимум, int индекс) МаксимумВДиапазоне(int[] массив, int a, int b)
        {
            int максимум = массив[a];
            int индекс = a;
            for (int i = a + 1; i <= b && i < массив.Length; i++)
            {
                if (массив[i] > максимум)
                {
                    максимум = массив[i];
                    индекс = i;
                }
            }
            return (максимум, индекс);
        }     
        static (int минимум, int индекс) МинимумНаЧетныхПозициях(int[] массив)
        {
            int минимум = массив[0];
            int индекс = 0;
            for (int i = 2; i < массив.Length; i += 2)
            {
                if (массив[i] < минимум)
                {
                    минимум = массив[i];
                    индекс = i;
                }
            }
            return (минимум, индекс);
        }   
        static (int минимум, int индекс) МинимумВДиапазоне(int[] массив, int a, int b)
        {
            int минимум = массив[a];
            int индекс = a;
            for (int i = a + 1; i <= b && i < массив.Length; i++)
            {
                if (массив[i] < минимум)
                {
                    минимум = массив[i];
                    индекс = i;
                }
            }
            return (минимум, индекс);
        }  
        static double СреднееНечетныхИндексов(int[] массив)
        {
            int сумма = 0;
            int количество = 0;
            for (int i = 1; i < массив.Length; i += 2)
            {
                сумма += массив[i];
                количество++;
            }
            return (double)сумма / количество;
        }
        static double СреднееНеПревышающих50(int[] массив)
        {
            int сумма = 0;
            int количество = 0;
            foreach (int элемент in массив)
            {
                if (элемент <= 50)
                {
                    сумма += элемент;
                    количество++;
                }
            }
            return (double)сумма / количество;
        }
        static (int[] четныеИндексы, int[] нечетныеИндексы) РазделитьПоИндексам(int[] массив)
        {
            int четныеИндексы = (массив.Length + 1) / 2;
            int нечетныеИндексы = массив.Length / 2;      
            int[] четные = new int[четныеИндексы];
            int[] нечетные = new int[нечетныеИндексы];   
            int четIndex = 0, нечетIndex = 0;
            for (int i = 0; i < массив.Length; i++)
            {
                if (i % 2 == 0)
                    четные[четIndex++] = массив[i];
                else
                    нечетные[нечетIndex++] = массив[i];
            }
            return (четные, нечетные);
        }
        static (int[] четныеНечетныеИндексы, int[] нечетныеЧетныеИндексы) РазделитьПоУсловию(int[] массив)
        {
            int четныеНечетные = 0;
            int нечетныеЧетные = 0;
            foreach (int элемент in массив)
            {
                if (элемент % 2 == 0 && (Array.IndexOf(массив, элемент) % 2 != 0))
                    четныеНечетные++;
                if (элемент % 2 != 0 && (Array.IndexOf(массив, элемент) % 2 == 0))
                    нечетныеЧетные++;
            }
            int[] массив1 = new int[четныеНечетные];
            int[] массив2 = new int[нечетныеЧетные]; 
            int index1 = 0, index2 = 0;
            for (int i = 0; i < массив.Length; i++)
            {
                if (массив[i] % 2 == 0 && i % 2 != 0)
                    массив1[index1++] = массив[i];
                if (массив[i] % 2 != 0 && i % 2 == 0)
                    массив2[index2++] = массив[i];
            }
            return (массив1, массив2);
        }
        static void Main(string[] args)
        {
            Console.Write("Введите размер массива n: ");
            int n = int.Parse(Console.ReadLine());   
            int[] массив = СоздатьМассив(n);
            Console.WriteLine("Исходный массив:");
            foreach (int элемент in массив)
            {
                Console.Write(элемент + " ");
            }
            Console.WriteLine();
            Console.WriteLine($"Сумма элементов с четными индексами: {СуммаЧетныхИндексов(массив)}");
            Console.WriteLine($"Минимальный положительный: {МинимальныйПоложительный(массив)}");
            Console.WriteLine($"Сумма максимального и минимального: {СуммаМаксМин(массив)}");
            Console.WriteLine($"Количество положительных: {КоличествоПоложительных(массив)}");
            Console.Write("Введите a: ");
            int a = int.Parse(Console.ReadLine());
            Console.Write("Введите b: ");
            int b = int.Parse(Console.ReadLine());
            Console.WriteLine($"Сумма четных положительных с {a} по {b}: {СуммаЧетныхПоложительныхВДиапазоне(массив, a, b)}");
            Console.WriteLine($"Среднее четных: {СреднееЧетных(массив):F2}");
            Console.WriteLine($"Сумма четных положительных: {СуммаЧетныхПоложительных(массив)}");
            Console.WriteLine($"Сумма неотрицательных на четных местах: {СуммаНеотрицательныхНаЧетныхМестах(массив)}");
            var (макс, индексМакс) = МаксимумВДиапазоне(массив, a, b);
            Console.WriteLine($"Максимум с {a} по {b}: {макс}, индекс: {индексМакс}");
            var (мин, индексМин) = МинимумНаЧетныхПозициях(массив);
            Console.WriteLine($"Минимум на четных позициях: {мин}, индекс: {индексМин}");
            var (минДиап, индексМинДиап) = МинимумВДиапазоне(массив, a, b);
            Console.WriteLine($"Минимум с {a} по {b}: {минДиап}, индекс: {индексМинДиап}");
            Console.WriteLine($"Среднее нечетных индексов: {СреднееНечетныхИндексов(массив):F2}");
            Console.WriteLine($"Среднее не превышающих 50: {СреднееНеПревышающих50(массив):F2}");
            var (четныеИндексы, нечетныеИндексы) = РазделитьПоИндексам(массив);
            Console.Write("Элементы с четными индексами: ");
            foreach (int эл in четныеИндексы) Console.Write(эл + " ");
            Console.WriteLine();
            Console.Write("Элементы с нечетными индексами: ");
            foreach (int эл in нечетныеИндексы) Console.Write(эл + " ");
            Console.WriteLine();
            var (массив1, массив2) = РазделитьПоУсловию(массив);
            Console.Write("Четные элементы с нечетными индексами: ");
            foreach (int эл in массив1) Console.Write(эл + " ");
            Console.WriteLine();
            Console.Write("Нечетные элементы с четными индексами: ");
            foreach (int эл in массив2) Console.Write(эл + " ");
            Console.WriteLine();
            Console.ReadKey();
        }
    }
}